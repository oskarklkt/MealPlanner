package mealplanner;

public class DailyPlan {
    String dayOfTheWeek;
    String breakfast;
    String lunch;
    String dinner;

    public DailyPlan(String dayOfTheWeek, String breakfast, String lunch, String dinner) {
        this.dayOfTheWeek = dayOfTheWeek;
        this.breakfast = breakfast;
        this.lunch = lunch;
        this.dinner = dinner;
    }
}
